    private void btnAddKiHocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddKiHocActionPerformed
        if(validateKiHoc()){
            KiHoc kh = this.readForm();
            if(khsv.checkTrungID(kh.getId())){
                JOptionPane.showMessageDialog(this, "Lỗi Trùng mã");
                return;
            }
            if(khsv.add(kh)!= 0){
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                fillTable(khsv.getAll());
            }else{
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
                return;
            }
        }
    }//GEN-LAST:event_btnAddKiHocActionPerformed

