    public void setTable() {
        model = (DefaultTableModel) jtbKiHoc.getModel();
        Object[] listTable = {"ID", "Tên Kỳ Học", "Năm Học"};
        model.setColumnIdentifiers(listTable);
        jtbKiHoc.setModel(model);
    }

    public void fillTable(List<KiHoc> list) {
        model = (DefaultTableModel) jtbKiHoc.getModel();
        model.setRowCount(0);
        for (KiHoc kiHoc : list) {
            model.addRow(kiHoc.toDataRow());
        }
    }

    public void showData(int index) {
        KiHoc kh = khsv.getAll().get(index);
        txtIDKiHoc.setText(kh.getId());
        txtTenKyHoc.setText(kh.getTenKyHoc());
        txtNamHoc.setText(String.valueOf(kh.getNamHoc()));
    }

    public boolean validateKiHoc() {
        if (txtIDKiHoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID không được để trống", "Error", 2);
            return false;
        }
        if (txtTenKyHoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên kì học không được để trống", "Error", 2);
            return false;
        }

        if (txtNamHoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên kì học không được để trống", "Error", 2);
            return false;
        } else {
            try {
                int kyHoc = Integer.parseInt(txtNamHoc.getText());
                if (Integer.parseInt(txtNamHoc.getText()) < 0 || Integer.parseInt(txtNamHoc.getText()) > 2023) {
                    JOptionPane.showMessageDialog(this, "Lỗi dữ liệu năm học");
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi dữ liệu kì học");
                return false;
            }

        }
        return true;
    }

    KiHoc readForm() {
        String id = txtIDKiHoc.getText();
        String tenKiHoc = txtTenKyHoc.getText();
        int namHoc = Integer.parseInt(txtNamHoc.getText());
        return new KiHoc(id, tenKiHoc, namHoc);
    }

